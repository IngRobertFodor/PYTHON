import math


# math.pi()

print(math.pi)
# Output: 3.141592653589793


# math.ceil()

print(math.ceil(3.15159))
# Output: 4


# math.floor()

print(math.floor(3.15159))
# Output: 3


# math.sqrt()

print(math.sqrt(16))
# Output: 4.0
print(math.sqrt(25))
# Output: 5.0
print(math.sqrt(8.25))
# Output: 2.8722813232690143


# math.factorial()

print(math.factorial(5))
# Output: 120